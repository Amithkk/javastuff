class pattern12
    {
        public static void main()
            {
                for(int i=4; i>0; --i) {
                    for(int j=1; j<=i;j++)
                        System.out.print(j);
                    for(int k=0; k<(4-i)*2; k++)
                        System.out.print(" ");
                    for(int l=i; l>0;l--)
                        System.out.print(l);
                    System.out.println();
                }
                for(int i=1; i<=4;i++){
                    for(int j=1;j<=i;j++)
                        System.out.print(j);
                    for(int k=0; k<(4-i)*2; k++)
                        System.out.print(" ");
                    for(int l=i;l>0;l--)
                        System.out.print(l);
                    System.out.println();
                    }
                }
            }